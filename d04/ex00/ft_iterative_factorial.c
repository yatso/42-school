int	ft_iterative_factorial(int nb)
{
	int	a;

	a = 1;
	if (nb <= 1)
	{
		return (a);
	}
	while (nb > 1)
	{
		a = a * nb;
		nb = nb - 1;
	}
	return (a);
}
